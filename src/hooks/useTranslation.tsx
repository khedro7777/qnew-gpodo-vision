export const useTranslation = () => {
  const { locale } = useRouter();
  const intl = createIntl(
    {
      locale: locale || 'en',
      messages: translations[locale || 'en'],
    },
    cache
  );
  const isRTL = intl.formatMessage({ id: 'dir' }) === 'rtl';

  return {
    t: intl.formatMessage,
    locale: locale || 'en',
    isRTL,
  };
};
